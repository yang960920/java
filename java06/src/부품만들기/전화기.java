package 부품만들기;

public class 전화기 {
	// 성질 : 멤버변수(가격,모양)
	// public은 아무데서나(다른패키지에서도) 접근 가능 설정 (접근제어자)
	public int price; // 멤버 변수는 자동으로 초기화해서 쓰레기 값이 없다!! (0)
	public String shape; // 기본형이 아닌 경우들은 null로 초기화 돼있음
	// 동작 : 멤버메서드(=함수), 동적인 기능을 수행한다.
	//		인터넷하다, 카톡하다.
	public void 인터넷하다() {
		// 기능 처리하는 방법을 나열
		System.out.println("와이파이연결하다.");
		System.out.println("데이터로연결하다.");
	}
	public void 카톡하다() {
		System.out.println("자바 단체톡을 하다.");
		System.out.println("게시물을 올리다.");
	}
}
